//数据帧处理
u8* FrameDeal(u8* FrameData)
{
  u8 *pTX  = 0;
  //待发送的数据地址 
  //帧检查   
  if( FrameCheck(FrameData) != 1)
  {
    return 0;
  } 
 
  //异常帧处理
  //寄存器地址错误
  //读取长度错误
  //非法指令 
  //帧处理 
  
  switch(FrameData[1])
  {
    case 0x03://读命令 
                    pTX = ModBus_Command03(FrameData); 
                    break;
    case 0x06://单数据写入 
                    pTX = ModBus_Command06(FrameData); 
                    break;
    case 0x10://连续数据写入 
                    pTX = ModBus_Command16(FrameData);
                    break;
    default: 
                    pTX = 0;
                    break;	
  }	
  return pTX;	
} 

//获取回复数据帧的长度
u8 GetFrameLen(u8* FrameData)
{
  if(FrameData[1] == 0x03)
  {
    return (FrameData[2] + 5);
  }
  else if(FrameData[1] == 0x06)
  {
    return 8;
  }
  else if(FrameData[1] == 0x10)
  {
    return 8;
  }  
  
  return 0;
}




