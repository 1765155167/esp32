/****************************************************************************
* 名    称：CheckCRC16
* 功    能：crc 校验
* 入口参数：u8*ucDataBuff：接收的数组,u16 ucDataLength：数组长度
* 出口参数：无
* 说    明：
* 调用方法：无 
****************************************************************************/
u16 CheckCRC16(u8 *ucDataBuff,u16 ucDataLength)
{
  u8 uchCRCHi = 0xFF ; 
  u8 uchCRCLo = 0xFF ;  
  u8 uIndex = 0; 
  
  while (ucDataLength--) 
  {    
    uIndex = uchCRCHi ^ *ucDataBuff++ ;   
    uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ;    
    uchCRCLo = auchCRCLo[uIndex] ;    
  }   
  
  return (u16)((u16)uchCRCHi << 8 | uchCRCLo); 
}

//指令校验 
u8 FrameCheck(u8* FrameData)
{
  u16 TempCrc1 = 0,TempCrc2 = 0;
  u16 length = 0;
  //检查ID是否正确
  if( FrameData[0] != SlaveAddr) 
  {
    return 0; 
  } 
  //检查CRC
  if( (FrameData[1] == 0x03) || (FrameData[1] == 0x06))//数据读取和单数据写入
  {
    TempCrc1 = CheckCRC16(FrameData,6);
    TempCrc2 = FrameData[6]*256 + FrameData[7];
    if(TempCrc1 != TempCrc2)
    {
      return 0;
    }
  } 
  else if( FrameData[1] == 0x10)//数据连续写入 
  {
    length = (FrameData[4]*256 + FrameData[5])*2 + 7;	
    TempCrc1 = CheckCRC16(FrameData,length);
    TempCrc2 = FrameData[length]*256 + FrameData[length+1];
    if(TempCrc1 != TempCrc2)
    {
      return 0;
    }	
  }
  return 1; 
}

//读取指令 
u8* ModBus_Command03(u8* FrameData)
{
  u16 Addr = 0;
  u16 CRC16=0xffff; 
  u8 OffAddr = 3;
  u8 StartAddr = FrameData[3]; 
  u8 RegNum =  FrameData[5];
  u16 EndAddr=StartAddr+RegNum*2;//计算最后一个被读取的寄存器地址
   
  ModbusTXBuf[0] = SlaveAddr;//返回地址     
  ModbusTXBuf[1] = 0x03;//返回功能码 03  
  ModbusTXBuf[2] = RegNum%256*2;//返回数据区字节数(2*寄存器数)       
  
  for(Addr = StartAddr;Addr < EndAddr;Addr++)     //按照命令要求依次返回数据
  {
    ModbusTXBuf[OffAddr++] = Register[Addr];//RegNum个寄存器，依次发送
  }
  
  CRC16 = CheckCRC16(ModbusTXBuf,OffAddr); 
   
  ModbusTXBuf[OffAddr++] = CRC16/256; //CRC,低位在先
  ModbusTXBuf[OffAddr++] = CRC16%256;
  
  return ModbusTXBuf;
}

//单数据写入 
u8* ModBus_Command06(u8* FrameData)
{           
  u8 WR_Addr = FrameData[3];
  u8 i = 0;
   
  //将数据存入相应的寄存器中
  Register[WR_Addr]= FrameData[4];    
  Register[WR_Addr+1]= FrameData[5];      
  
  for(i = 0;i < 8;i++)  
  {
    ModbusTXBuf[i] = FrameData[i];
  } 
  ChangeData = 1;
  return ModbusTXBuf;	
}

//连续写入 
u8* ModBus_Command16(u8* FrameData)
{
  u8 StartAddr = FrameData[3]; 
  u8 RegNum =  FrameData[5];
  u8 OffAddr = 7;
  u16 EndAddr=StartAddr+RegNum*2;   //计算最后一个被写入的寄存器地址
  u16 Addr,i=0;
  u16 CRC16=0xffff; 
  
  for(Addr=StartAddr;Addr<EndAddr;Addr++)  //从起始地址开始，依次写入
  {
    Register[Addr] =  FrameData[OffAddr++];
  } 
  for(i = 0;i < 6;i++)
  {
    ModbusTXBuf[i] = FrameData[i]; 
  }
  CRC16 = CheckCRC16(ModbusTXBuf,6); 
   
  ModbusTXBuf[6] = CRC16/256; //CRC,低位在先
  ModbusTXBuf[7] = CRC16%256;
  
  ChangeData = 1;
  return ModbusTXBuf;
}

